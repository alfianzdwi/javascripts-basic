let x = 9
let y = 20


//Operator Aritmatika
console.log(x += y) //Diri Sendiri Penjumlahan
console.log(y / x)


//Operator Perbandingan
console.log(x != y)
console.log(x < y) // Nilai sudah berubah jadi 29

//Operator Logika
console.log(x > 20 && y < 5) //Operator AND kedua kondisi harus True jika ingin mendapatkan Nilai True
console.log(x > 20 || y < 5) //Operator OR salah satu nilai harus True jika ingin mendapatkan Nilai True
console.log(!(x > 20 || y < 50)) //Operator NOT untuk membalikan dari nilai hasil sebenarnya